/// Motion output (new state after one control cycle).
pub struct OutputParameter {
    ptr: *mut ffi::SCattiOutputParameter,
    dofs: usize,
}

impl OutputParameter {
    pub fn new(dofs: usize) -> Self {
        let ptr = unsafe { ffi::scatti_output_create(dofs) };
        assert!(!ptr.is_null(), "scatti_output_create returned NULL");
        Self { ptr, dofs }
    }

    pub fn degrees_of_freedom(&self) -> usize {
        self.dofs
    }

    pub fn new_position(&self) -> &[f64] {
        unsafe { std::slice::from_raw_parts((*self.ptr).new_position, self.dofs) }
    }

    pub fn new_velocity(&self) -> &[f64] {
        unsafe { std::slice::from_raw_parts((*self.ptr).new_velocity, self.dofs) }
    }

    pub fn new_acceleration(&self) -> &[f64] {
        unsafe { std::slice::from_raw_parts((*self.ptr).new_acceleration, self.dofs) }
    }

    pub fn new_jerk(&self) -> &[f64] {
        unsafe { std::slice::from_raw_parts((*self.ptr).new_jerk, self.dofs) }
    }

    pub fn time(&self) -> f64 {
        unsafe { (*self.ptr).time }
    }

    pub fn new_section(&self) -> usize {
        unsafe { (*self.ptr).new_section }
    }

    pub fn did_section_change(&self) -> bool {
        unsafe { (*self.ptr).did_section_change }
    }

    pub fn new_calculation(&self) -> bool {
        unsafe { (*self.ptr).new_calculation }
    }

    pub fn was_calculation_interrupted(&self) -> bool {
        unsafe { (*self.ptr).was_calculation_interrupted }
    }

    pub fn calculation_duration(&self) -> f64 {
        unsafe { (*self.ptr).calculation_duration }
    }

    /// Access the computed trajectory.
    pub fn trajectory(&self) -> Trajectory<'_> {
        Trajectory {
            ptr: unsafe { (*self.ptr).trajectory },
            dofs: self.dofs,
            _marker: std::marker::PhantomData,
        }
    }

    /// Copy output state back into input for the next control cycle.
    pub fn pass_to_input(&self, inp: &mut InputParameter) {
        unsafe { ffi::scatti_output_pass_to_input(self.ptr, inp.as_ptr()); }
    }

    pub(crate) fn as_ptr(&mut self) -> *mut ffi::SCattiOutputParameter {
        self.ptr
    }
}

impl Drop for OutputParameter {
    fn drop(&mut self) {
        if !self.ptr.is_null() {
            unsafe { ffi::scatti_output_destroy(self.ptr); }
            self.ptr = ptr::null_mut();
        }
    }
}

