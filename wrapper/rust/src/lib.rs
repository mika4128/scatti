//! Safe Rust bindings for **scatti** — jerk-limited real-time trajectory
//! generation.
//!
//! # Example
//! ```no_run
//! use scatti::{Ruckig, InputParameter, OutputParameter, Result};
//!
//! let mut ruckig = Ruckig::new(3, 0.01);
//! let mut input = InputParameter::new(3);
//! let mut output = OutputParameter::new(3);
//!
//! input.current_position_mut().copy_from_slice(&[0.0, 0.0, 0.5]);
//! input.target_position_mut().copy_from_slice(&[5.0, -2.0, -3.5]);
//! input.max_velocity_mut().copy_from_slice(&[3.0, 1.0, 3.0]);
//! input.max_acceleration_mut().copy_from_slice(&[3.0, 2.0, 1.0]);
//! input.max_jerk_mut().copy_from_slice(&[4.0, 3.0, 2.0]);
//!
//! loop {
//!     let res = ruckig.update(&input, &mut output);
//!     println!("t={:.3} pos={:?}", output.time(), output.new_position());
//!     output.pass_to_input(&mut input);
//!     match res {
//!         Result::Finished => break,
//!         Result::Working => {}
//!         _ => panic!("error"),
//!     }
//! }
//! ```

#![allow(non_upper_case_globals, non_camel_case_types, non_snake_case)]

/// Raw FFI bindings generated by bindgen.
pub mod ffi {
    include!(concat!(env!("OUT_DIR"), "/bindings.rs"));
}

use std::ptr;

// ---------------------------------------------------------------------------
// Result
// ---------------------------------------------------------------------------

/// Trajectory computation result.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
#[repr(i32)]
pub enum Result {
    Working = 0,
    Finished = 1,
    Error = -1,
    ErrorInvalidInput = -100,
    ErrorTrajectoryDuration = -101,
    ErrorPositionalLimits = -102,
    ErrorZeroLimits = -104,
    ErrorExecutionTimeCalculation = -110,
    ErrorSynchronizationCalculation = -111,
}

impl From<ffi::SCattiResult> for Result {
    fn from(v: ffi::SCattiResult) -> Self {
        match v {
            0 => Result::Working,
            1 => Result::Finished,
            -100 => Result::ErrorInvalidInput,
            -101 => Result::ErrorTrajectoryDuration,
            -102 => Result::ErrorPositionalLimits,
            -104 => Result::ErrorZeroLimits,
            -110 => Result::ErrorExecutionTimeCalculation,
            -111 => Result::ErrorSynchronizationCalculation,
            _ => Result::Error,
        }
    }
}

// ---------------------------------------------------------------------------
// ControlInterface / Synchronization / DurationDiscretization
// ---------------------------------------------------------------------------

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[repr(u32)]
pub enum ControlInterface {
    Position = 0,
    Velocity = 1,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[repr(u32)]
pub enum Synchronization {
    Time = 0,
    TimeIfNecessary = 1,
    Phase = 2,
    No = 3,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[repr(u32)]
pub enum DurationDiscretization {
    Continuous = 0,
    Discrete = 1,
}

// ---------------------------------------------------------------------------
// InputParameter
// ---------------------------------------------------------------------------

/// Motion input parameters (current/target state & limits).
pub struct InputParameter {
    ptr: *mut ffi::SCattiInputParameter,
    dofs: usize,
}

macro_rules! input_array_accessors {
    ($getter:ident, $getter_mut:ident, $field:ident) => {
        pub fn $getter(&self) -> &[f64] {
            unsafe { std::slice::from_raw_parts((*self.ptr).$field, self.dofs) }
        }
        pub fn $getter_mut(&mut self) -> &mut [f64] {
            unsafe { std::slice::from_raw_parts_mut((*self.ptr).$field, self.dofs) }
        }
    };
}

impl InputParameter {
    pub fn new(dofs: usize) -> Self {
        let ptr = unsafe { ffi::scatti_input_create(dofs) };
        assert!(!ptr.is_null(), "scatti_input_create returned NULL");
        Self { ptr, dofs }
    }

    pub fn degrees_of_freedom(&self) -> usize {
        self.dofs
    }

    input_array_accessors!(current_position, current_position_mut, current_position);
    input_array_accessors!(current_velocity, current_velocity_mut, current_velocity);
    input_array_accessors!(current_acceleration, current_acceleration_mut, current_acceleration);
    input_array_accessors!(target_position, target_position_mut, target_position);
    input_array_accessors!(target_velocity, target_velocity_mut, target_velocity);
    input_array_accessors!(target_acceleration, target_acceleration_mut, target_acceleration);
    input_array_accessors!(max_velocity, max_velocity_mut, max_velocity);
    input_array_accessors!(max_acceleration, max_acceleration_mut, max_acceleration);
    input_array_accessors!(max_jerk, max_jerk_mut, max_jerk);

    // --- optional arrays ---
    pub fn set_min_velocity(&mut self, values: &[f64]) {
        assert_eq!(values.len(), self.dofs);
        unsafe {
            if (*self.ptr).min_velocity.is_null() {
                (*self.ptr).min_velocity = libc_calloc_f64(self.dofs);
            }
            ptr::copy_nonoverlapping(values.as_ptr(), (*self.ptr).min_velocity, self.dofs);
        }
    }

    pub fn set_min_acceleration(&mut self, values: &[f64]) {
        assert_eq!(values.len(), self.dofs);
        unsafe {
            if (*self.ptr).min_acceleration.is_null() {
                (*self.ptr).min_acceleration = libc_calloc_f64(self.dofs);
            }
            ptr::copy_nonoverlapping(values.as_ptr(), (*self.ptr).min_acceleration, self.dofs);
        }
    }

    pub fn set_enabled(&mut self, values: &[bool]) {
        assert_eq!(values.len(), self.dofs);
        unsafe {
            if (*self.ptr).enabled.is_null() {
                (*self.ptr).enabled = libc::calloc(self.dofs, std::mem::size_of::<bool>()) as *mut bool;
            }
            for i in 0..self.dofs {
                *(*self.ptr).enabled.add(i) = values[i];
            }
        }
    }

    // --- enum properties ---
    pub fn set_control_interface(&mut self, ci: ControlInterface) {
        unsafe { (*self.ptr).control_interface = ci as u32; }
    }

    pub fn set_synchronization(&mut self, sync: Synchronization) {
        unsafe { (*self.ptr).synchronization = sync as u32; }
    }

    pub fn set_duration_discretization(&mut self, dd: DurationDiscretization) {
        unsafe { (*self.ptr).duration_discretization = dd as u32; }
    }

    pub fn set_minimum_duration(&mut self, d: Option<f64>) {
        unsafe {
            match d {
                Some(v) => {
                    (*self.ptr).minimum_duration = v;
                    (*self.ptr).has_minimum_duration = true;
                }
                None => {
                    (*self.ptr).has_minimum_duration = false;
                }
            }
        }
    }

    // --- Pro features ---

    /// Set intermediate waypoints. Each inner slice must have `dofs` elements.
    pub fn set_intermediate_positions(&mut self, waypoints: &[&[f64]]) {
        if waypoints.is_empty() {
            unsafe { ffi::scatti_input_set_intermediate_positions(self.ptr, ptr::null(), 0); }
            return;
        }
        let n = waypoints.len();
        let mut flat = vec![0.0; n * self.dofs];
        for (i, wp) in waypoints.iter().enumerate() {
            assert_eq!(wp.len(), self.dofs, "waypoint {i} has wrong size");
            flat[i * self.dofs..(i + 1) * self.dofs].copy_from_slice(wp);
        }
        unsafe {
            ffi::scatti_input_set_intermediate_positions(self.ptr, flat.as_ptr(), n);
        }
    }

    pub fn set_max_position(&mut self, values: &[f64]) {
        assert_eq!(values.len(), self.dofs);
        unsafe {
            if (*self.ptr).max_position.is_null() {
                (*self.ptr).max_position = libc_calloc_f64(self.dofs);
            }
            ptr::copy_nonoverlapping(values.as_ptr(), (*self.ptr).max_position, self.dofs);
        }
    }

    pub fn set_min_position(&mut self, values: &[f64]) {
        assert_eq!(values.len(), self.dofs);
        unsafe {
            if (*self.ptr).min_position.is_null() {
                (*self.ptr).min_position = libc_calloc_f64(self.dofs);
            }
            ptr::copy_nonoverlapping(values.as_ptr(), (*self.ptr).min_position, self.dofs);
        }
    }

    pub fn set_interrupt_calculation_duration(&mut self, duration_us: f64) {
        unsafe { (*self.ptr).interrupt_calculation_duration = duration_us; }
    }

    pub fn validate(&self, check_current: bool, check_target: bool) -> bool {
        unsafe { ffi::scatti_input_validate(self.ptr, check_current, check_target) }
    }

    pub(crate) fn as_ptr(&self) -> *mut ffi::SCattiInputParameter {
        self.ptr
    }
}

impl Drop for InputParameter {
    fn drop(&mut self) {
        if !self.ptr.is_null() {
            unsafe { ffi::scatti_input_destroy(self.ptr); }
            self.ptr = ptr::null_mut();
        }
    }
}

