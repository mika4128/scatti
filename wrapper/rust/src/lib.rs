fn libc_calloc_f64(n: usize) -> *mut f64 {
    unsafe { libc::calloc(n, std::mem::size_of::<f64>()) as *mut f64 }
}

// Re-export libc for internal use
mod libc {
    extern "C" {
        pub fn calloc(nmemb: usize, size: usize) -> *mut std::ffi::c_void;
    }
}
